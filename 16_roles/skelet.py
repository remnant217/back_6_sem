async def authenticate_user(
    session: AsyncSession,
    username: str,
    password: str
) -> User | None:
    user = await get_user_by_username(session, username)
    # –µ—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å —Ç–∞–∫–∏–º username –Ω–µ—Ç –≤ –ë–î
    if user is None:
        return None
    # –µ—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –µ—Å—Ç—å - –ø—Ä–æ–≤–µ—Ä—è–µ–º –µ–≥–æ –ø–∞—Ä–æ–ª—å
