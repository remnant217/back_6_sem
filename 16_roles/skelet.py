# –≤ tokenUrl —É–∫–∞–∑—ã–≤–∞–µ–º –∞–¥—Ä–µ—Å —ç–Ω–¥–ø–æ–∏–Ω—Ç–∞, –≥–¥–µ –≤ –±—É–¥—É—â–µ–º –±—É–¥–µ–º –±—Ä–∞—Ç—å —Ç–æ–∫–µ–Ω
reusable_oauth2 = OAuth2PasswordBearer(tokenUrl="/login/access-token")

