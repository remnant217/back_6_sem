    # –µ—Å–ª–∏ –ø–∞—Ä–æ–ª—å –Ω–µ —Å–æ–≤–ø–∞–ª
    if not verified:
        return None
    # –µ—Å–ª–∏ –ø–æ–ª—É—á–∏–ª–∏ –Ω–æ–≤—ã–π —Ö—ç—à –∏–∑-–∑–∞ –Ω–æ–≤–æ–≥–æ –∞–ª–≥–æ—Ä–∏—Ç–º–∞ —Ö—ç—à–∏—Ä–æ–≤–∞–Ω–∏—è - –æ–±–Ω–æ–≤–ª—è–µ–º –µ–≥–æ –≤ –ë–î
    if updated_hash:
        user.hashed_password = updated_hash
        session.add(user)
        await session.commit()
        await session.refresh(user)
    return user

