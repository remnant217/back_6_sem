def _apply_items_filters(stmt, q: str | None, user_id: UUID | None):
    if user_id is not None:
        stmt = stmt.where(Item.user_id == user_id)
    if q:
        q = q.strip()
        if q:
            stmt = stmt.where(Item.title.ilike(f'%{q}%'))
    return stmt

